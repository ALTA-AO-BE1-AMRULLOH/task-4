let num = 50
if (num >= 80 && num <= 100) {
    console.log("A");
} else if (num >= 65 && num <= 79) {
    console.log("B+");
} else if (num >= 50 && num <= 64) {
    console.log("B");
} else if (num >= 35 && num <= 49) {
    console.log("C");
} else if (num >= 0 && num <= 34) {
    console.log("D");
} else {
    console.log("Invalid");
}
